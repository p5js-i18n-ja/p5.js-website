function setup() {
  // キャンバスを作成します
  createCanvas(720, 400);

  // スクリーンリーダー用の説明を作成します
  textOutput();
}

function draw() {
  // 背景をクリアします
  background(0);

  // 青い四角形を描画します
  // 現在の座標系を保存します
  push();

  // 2倍にスケーリングします
  scale(2);

  // 色を青に設定します
  fill(33, 89, 194);

  // 原点に四角形を描画します（サイズ200）
  square(0, 0, 200);

  // 座標系を復元します
  pop();

  // 白い四角形を描画します
  // 色を白に設定します
  fill(255);

  // 原点に四角形を描画します（サイズ200）
  square(0, 0, 200);

  // 緑の四角形を描画します
  // 現在の座標系を保存します
  push();

  // x方向に0.5倍、y方向に0.75倍にスケーリングします
  scale(0.5, 0.75);

  // 色を緑に設定します
  fill(42, 150, 60);

  // 原点に四角形を描画します（サイズ200）
  square(0, 0, 200);

  // 座標系を復元します
  pop();
}
